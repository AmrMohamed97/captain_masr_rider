class UserModel {
  final int? id, isRider, defaultPaymentMethodId, defaultSubPaymentMethodId;
  final String? token,
      username,
      email,
      phone,
      countryCode,
      country,
      gender,
      profilePicture,
      refferalCode,
      idNumber,
      nationalIdImage,
      nationalIdExpiry,
      defaultPaymentMethodName,
      defaultPaymentMethodLogo;
  final num? rating, points;
  final bool? emailVerified, isConvertedToDriverBefore;

  UserModel({
    required this.id,
    required this.isRider,
    required this.token,
    required this.username,
    required this.email,
    required this.phone,
    required this.countryCode,
    required this.country,
    required this.gender,
    required this.profilePicture,
    required this.refferalCode,
    required this.rating,
    required this.points,
    required this.idNumber,
    required this.nationalIdImage,
    required this.nationalIdExpiry,
    required this.emailVerified,
    required this.isConvertedToDriverBefore,
    required this.defaultPaymentMethodId,
    required this.defaultPaymentMethodName,
    required this.defaultPaymentMethodLogo,
    required this.defaultSubPaymentMethodId,
  });

  factory UserModel.fromJson(Map<String, dynamic> json) {
    return UserModel(
      id: json["id"],
      isRider: json["is_rider"],
      token: json["token"],
      username: json["username"] ?? json["name"],
      email: json["email"],
      phone: json["phone"],
      countryCode: json["country_code"],
      country: json["country"],
      gender: json["gender"],
      profilePicture: (json["profile_picture"] ?? json["image"]).toString(),
      refferalCode: json["refferal_code"],
      rating: json["rating"],
      points: json["points"],
      idNumber: json["ID_number"],
      nationalIdImage: json["national_id"],
      nationalIdExpiry: json["national_id_expiry"],
      emailVerified: json["email_verified"],
      isConvertedToDriverBefore: json["is_converted_to_driver_before"],
      defaultPaymentMethodId: json["default_payment_method_id"],
      defaultPaymentMethodName: json["default_payment_method_name"],
      defaultPaymentMethodLogo: json["default_payment_method_logo"],
      defaultSubPaymentMethodId: json["default_sub_payment_method_id"],
    );
  }

  Map toJson() => {
        "id": id,
        "is_rider": isRider,
        "token": token,
        "username": username,
        "email": email,
        "phone": phone,
        "country_code": countryCode,
        "country": country,
        "gender": gender,
        "profile_picture": profilePicture,
        "refferal_code": refferalCode,
        "rating": rating,
        "points": points,
        "ID_number": idNumber,
        "national_id": nationalIdImage,
        "national_id_expiry": nationalIdExpiry,
        "is_converted_to_driver_before": isConvertedToDriverBefore,
        "default_payment_method_id": defaultPaymentMethodId,
        "default_payment_method_name": defaultPaymentMethodName,
        "default_payment_method_logo": defaultPaymentMethodLogo,
        "default_sub_payment_method_id": defaultSubPaymentMethodId,
      };
}
